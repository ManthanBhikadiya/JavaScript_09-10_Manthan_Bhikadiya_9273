// JavaScript Math Methods

// Math.abs(x)
{
    let math = Math.abs(23.45)
    console.log(math);

    let math1 = Math.abs(-23.45)
    console.log(math1);
}

// Math.ceil(x)
{
    let math = Math.ceil(3.68)
    console.log(math);

    let math1 = Math.ceil(-5.95)
    console.log(math1);
}

// Math.cos(x)
{
    let math = Math.cos(90 * Math.PI / 180)
    console.log(math);
}

// Math.sin(x)
{
    let math = Math.sin(30 * Math.PI / 180)
    console.log(math);
}

// Math.log(x)
{
    let math = Math.log(5)
    console.log(math);
}

// Math.log2(x)
{
    let math = Math.log2(5)
    console.log(math);
}

// Math.log10(x)
{
    let math = Math.log10(5)
    console.log(math);
}

// Math.max()
// Math.max(value1)
// Math.max(value1, value2)
// Math.max(value1, value2, /* …, */ valueN)
{
    let math = Math.max(5, 10, 24, 50, 74, 89)
    console.log(math);
    let math1 = Math.max(-5, -10)
    console.log(math1);
}

// Math.min()
// Math.min(value1)
// Math.min(value1, value2)
// Math.min(value1, value2, /* …, */ valueN)
{
    let math = Math.min(5, 10, 24, 50, 74, 89)
    console.log(math);
    let math1 = Math.min(-5, -10)
    console.log(math1);
}

// Math.pow(base, exponent)
{
    let math = Math.pow(5, 5)
    console.log(math);

    let math1 = Math.pow(5, -5)
    console.log(math1);
}

// Math.random()
{
    let math = Math.floor(Math.random() * 10000)
    console.log(math);
}

// Math.round(x)
{
    let math = Math.round(4.59)
    console.log(math);
}

// Math.sqrt(x)
{
    let math = Math.sqrt(4)
    console.log(math);
}

// Math.trunc(x)
{
    let math = Math.trunc(40.87654)
    console.log(math);

    let math1 = Math.trunc(-40.87654)
    console.log(math1);
}